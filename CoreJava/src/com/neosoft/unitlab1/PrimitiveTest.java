//Write a Java program to display default value of all primitive data types of Java.
package com.neosoft.unitlab1;

public class PrimitiveTest {
	//static veriable
	private static int a;
	private static byte b;
	private static short c;
	private static long d;
	private static float e;
	private static double f;
	private static boolean g;
	private static char ch;

	public static void main(String[] args) {
		System.out.println("Default value of Int :- "+a);
		System.out.println("Default value of byte :- "+b);
		System.out.println("Default value of short :- "+c);
		System.out.println("Default value of long :- "+d);
		System.out.println("Default value of float :- "+e);
		System.out.println("Default value of double :- "+f);
		System.out.println("Default value of boolean :- "+g);
		System.out.println("Default value of char :- "+ch);

	}

}
